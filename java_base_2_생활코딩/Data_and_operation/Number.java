
// 숫자형 데이터 타입  
// 사칙연산, 수학적 계산이 가능한 메소드 사용
public class Number {

	public static void main(String[] args) {

		// Operator연산자
		System.out.println(6 + 2);
		System.out.println(6 - 2);
		System.out.println(6 * 2);
		System.out.println(6 / 2);
		
		// Math 수학적 메소드
		
		System.out.println(Math.PI);
		// 내림 메소드
		System.out.println(Math.floor(Math.PI));
		// 올림 메소드
		System.out.println(Math.ceil(Math.PI));
	}

}
